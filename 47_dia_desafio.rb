#Escreva um programa que verifica se um número é um palíndromo (o número é o mesmo se lido de trás para frente).

input = '12321'

input_reverse = input.reverse

if input == input_reverse
  puts "Palíndromo"
else 
  puts "Não é Palíndromo"
end